<h3>AMRTS's Non-Discrimination Statement</h3>

<p>Arcata and Mad River Transit System (AMRTS) is committed to a policy of non-discrimination in program services pursuant to the requirements of Title VI of the Civil Rights Act of 1964.  Any person who believes that he or she has been subjected to discrimination under Title VI on the basis of race, color or national origin may file a complaint with AMRTS.  To receive additional information on AMRTS's non-discrimination obligations, or to file a complaint, please call the AMRTS administrative office at (707) 822-3775 
 or write to: Arcata and Mad River Transit System, 925 E Street, Arcata CA 95521.  Click on <a href="#procedures">Complaint & Procedures or Complaint Form</a> for details.<p>

<h3>AMRTS's Reglas de No Descriminacion</h3>

<p>Arcata and Mad River Transit System (AMRTS) se compromete a una norma de no discriminación en sus programas de servicio de acuerdo a los requisitos del Artículo VI del Acto Derechos Civiles de 1964. Cualquier persona que crea que ha sido discriminada bajo el Artículo VI basado en raza, color, u origen nacional puede asentar una demanda con AMRTS. Para recibir más información acerca de las obligaciones de no discriminación de AMRTS o para archivar una demanda, favor de llamar al (707) 822-3775 o escriba a: Arcata and Mad River Transit System, 925 E Street, Arcata CA 95521.</p>

<a name="procedures"></a><h3>Title VI Complaint Procedures</h3>

Should a complaint be filed with Arcata and Mad River Transit System and an external agency simultaneously, the external complaint shall supersede the Arcata and Mad River Transit System complaint and Arcata and Mad River Transit System's complaint procedures will be suspended pending the external agency's findings.

If filed with Arcata and Mad River Transit System, the Title VI Administrator will begin assessment or investigation of the complaint within fifteen (15) working days of receiving the complaint.  Based upon all of the information received, the Title VI Administrator will prepare a draft written response subject to review by the Arcata and Mad River Transit System Governing Board of Directors.  If more time is required, the Administrator shall notify the complaint of the estimated timeframe for completing the review, not to exceed (60) calendar days of the receipt of the formal complaint.  If appropriate, Arcata and Mad River Transit System may administratively close the complaint.

If final written response is determined to be needed, the complaint will receive a letter stating the final decision of the General Manager and the complaint will be advised of his/her right to file a complaint with the Federal Transit Administration (FTA),  Office of Civil Rights should the complaint feel dissatisfied with the decision.

<h4>Filing a Complaint with the Federal Transit Administration</h4>

<p>To file a complaint with the Federal Transit Administration, fill out a Title VI complaint form and mail it to:</p>

<blockquote>
Federal Transit Administration Office of Civil Rights
<br/>Attention: Title VI Program Coordinator
<br/>East Building, 5th Floor - TCR
<br/>1200 New Jersey Ave., SE
<br/>Washington, DC  20590
</blockquote>

<p>The <a href="http://www.fta.dot.gov/civilrights/title6/civilrights5104.html">complaint form may be downloaded from Federal Transit Administration</a> website.

<p>Upon request, assistance in the preparation of any necessary written material will be provided to a person or persons who are unable to read or write.</p>

